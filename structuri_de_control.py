# Structurile If,Else, For și While în Python
numar = 10
if numar > 5:
    print("Numărul este mai mare decât 5")
else:
    print("Numărul nu este mai mare decât 5")

# For loop
for i in range(1, 6):  #dacă range(1,6) generează 1, 2, 3, 4, 5
    print(i)

# While loop
contor = 0
while contor < 5:
    print(contor)
    contor += 1
